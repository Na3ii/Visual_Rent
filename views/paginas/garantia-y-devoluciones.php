  <h2 class="garantia__heading">Garantías y Devoluciones</h2>
  <p class="garantia__parrafo">
    En <strong>VisualRent.cl</strong> nos comprometemos a entregar equipos en excelentes condiciones de funcionamiento, listos para su uso inmediato. Nuestra prioridad es tu satisfacción, por lo que contamos con una política clara de garantías y devoluciones ante cualquier eventualidad técnica.
  </p>

  <h3>¿Qué cubre la garantía?</h3>
  <p class="garantia__parrafo">
    La garantía cubre fallas técnicas o defectos de funcionamiento del equipo que no hayan sido ocasionados por mal uso, accidentes o intervención no autorizada. En estos casos, ofrecemos:
  </p>
  <ul>
    <li>Reemplazo del equipo dañado por uno de características similares (sujeto a disponibilidad).</li>
    <li>Asistencia técnica inmediata en terreno o de forma remota.</li>
    <li>Devolución proporcional del valor del servicio si no se puede reemplazar el equipo durante el evento.</li>
  </ul>

  <h3>¿Qué no cubre?</h3>
  <ul>
    <li>Daños causados por uso indebido, golpes, exposición a líquidos o manipulación por personal no autorizado.</li>
    <li>Fallas generadas por condiciones externas como cortes de energía, problemas con la infraestructura del lugar o interferencias externas.</li>
    <li>Retrasos o fallos causados por falta de información técnica previa por parte del cliente.</li>
  </ul>

  <h3>Condiciones para solicitar garantía</h3>
  <ul>
    <li>El problema debe ser reportado dentro de las primeras 24 horas desde la entrega o inicio del evento.</li>
    <li>El cliente debe proporcionar evidencia (fotos, videos o una descripción detallada del problema).</li>
    <li>VisualRent SpA evaluará el caso y dará respuesta en un máximo de 48 horas hábiles.</li>
  </ul>

  <h3>Cancelaciones y devoluciones</h3>
  <ul>
    <li>Cancelaciones con más de 72 horas de anticipación: reembolso completo o crédito para otro evento.</li>
    <li>Cancelaciones entre 48 y 72 horas: devolución parcial del 50%.</li>
    <li>Cancelaciones con menos de 48 horas: no aplica reembolso por costos de logística, programación y asignación de recursos.</li>
  </ul>

  <p class="garantia__parrafo">
    Para solicitar una garantía, devolución o hacer seguimiento de un reclamo, escríbenos a <a href="mailto:contacto@visualrent.cl">contacto@visualrent.cl</a> con tu número de pedido, fecha del evento y una breve descripción del caso.
  </p>
