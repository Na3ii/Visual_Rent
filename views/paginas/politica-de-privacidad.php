  <h2 class="politica__heading">Política de Privacidad</h2>

  <p class="politica__parrafo">
    En <strong>VisualRent.cl</strong> valoramos y respetamos tu privacidad. Esta política describe cómo recopilamos, utilizamos, almacenamos y protegemos tus datos personales conforme a la legislación vigente en Chile y buenas prácticas internacionales de protección de datos.
  </p>

  <h3>¿Qué datos recopilamos?</h3>
  <p class="politica__parrafo">
    Podemos recopilar la siguiente información cuando interactúas con nuestro sitio web:
  </p>
  <ul class="politica__lista">
    <li><strong>Datos personales:</strong> nombre, correo electrónico, número de teléfono, empresa, dirección.</li>
    <li><strong>Datos técnicos:</strong> dirección IP, tipo de navegador, sistema operativo, proveedor de internet.</li>
    <li><strong>Datos de navegación:</strong> páginas visitadas, tiempo de permanencia, clics realizados.</li>
    <li><strong>Datos de cookies:</strong> comportamiento de navegación, preferencias del sitio, idioma.</li>
  </ul>

  <h3>¿Cómo usamos tus datos?</h3>
  <p class="politica__parrafo">
    Usamos tus datos para diversos fines legítimos, tales como:
  </p>
  <ul class="politica__lista">
    <li>Brindar atención y seguimiento a tus solicitudes o cotizaciones.</li>
    <li>Mejorar tu experiencia de navegación y personalizar el contenido del sitio.</li>
    <li>Enviar boletines informativos, promociones o información relevante si has dado tu consentimiento.</li>
    <li>Realizar análisis estadísticos y mejorar la calidad de nuestros servicios.</li>
    <li>Cumplir con obligaciones legales o requerimientos de autoridades competentes.</li>
  </ul>

  <h3>¿Compartimos tu información?</h3>
  <p class="politica__parrafo">
    No vendemos, alquilamos ni cedemos tus datos personales a terceros. Solo compartimos información cuando:
  </p>
  <ul class="politica__lista">
    <li>Es necesario para prestarte un servicio solicitado (por ejemplo, plataformas de email marketing).</li>
    <li>Existen requerimientos legales por parte de autoridades competentes.</li>
    <li>Contamos con tu consentimiento explícito.</li>
  </ul>

  <h3>Seguridad de la información</h3>
  <p class="politica__parrafo">
    Implementamos medidas técnicas y organizativas apropiadas para proteger tus datos personales contra pérdida, robo, acceso no autorizado, divulgación, alteración o destrucción. Entre ellas se incluyen protocolos SSL, firewalls y políticas de acceso restringido a la información.
  </p>

  <h3>Conservación de los datos</h3>
  <p class="politica__parrafo">
    Conservaremos tus datos personales durante el tiempo necesario para cumplir los fines para los que fueron recopilados, o según lo requiera la ley. Posteriormente, los eliminaremos de forma segura.
  </p>

  <h3>Tus derechos</h3>
  <p class="politica__parrafo">
    Como titular de tus datos, tienes derecho a:
  </p>
  <ul class="politica__lista">
    <li>Acceder a tus datos personales que almacenamos.</li>
    <li>Solicitar la rectificación o actualización de tus datos.</li>
    <li>Solicitar la eliminación de tus datos si ya no son necesarios para los fines establecidos.</li>
    <li>Revocar tu consentimiento en cualquier momento.</li>
  </ul>
  <p class="politica__parrafo">
    Para ejercer estos derechos, escríbenos a <a href="mailto:contacto@visualrent.cl">contacto@visualrent.cl</a>.
  </p>

  <h3>Cookies</h3>
  <p class="politica__parrafo">
    Este sitio utiliza cookies propias y de terceros para mejorar tu experiencia de usuario, analizar el tráfico y personalizar el contenido. Puedes obtener más información en nuestra <a href="/politica-de-cookies">Política de Cookies</a>.
  </p>

  <h3>Cambios en la política</h3>
  <p class="politica__parrafo">
    Nos reservamos el derecho a modificar esta política de privacidad en cualquier momento. Las modificaciones serán publicadas en esta misma página y entrarán en vigencia desde su publicación.
  </p>

