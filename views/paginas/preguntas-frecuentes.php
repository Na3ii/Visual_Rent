    <p class="faq__respuesta">
      Principalmente operamos en Santiago y regiones cercanas. Sin embargo, dependiendo del tipo de evento y la logística, podemos evaluar envíos a otras regiones de Chile. Te recomendamos contactarnos con antelación para coordinar.
    </p>
  </section>

  <section class="faq__item">
    <h3 class="faq__pregunta">¿Cómo puedo cotizar un servicio?</h3>
    <p class="faq__respuesta">
      Puedes llenar el formulario de contacto en nuestro sitio web, escribirnos directamente por WhatsApp o enviarnos un correo a <a href="mailto:contacto@visualrent.cl">contacto@visualrent.cl</a>. Recibirás una cotización personalizada según los requerimientos de tu evento.
    </p>
  </section>

  <section class="faq__item">
    <h3 class="faq__pregunta">¿Qué incluye el servicio de arriendo?</h3>
    <p class="faq__respuesta">
      Todos nuestros servicios incluyen entrega, instalación, asistencia técnica y retiro. En algunos casos también ofrecemos soporte en tiempo real durante el evento para asegurar que todo funcione correctamente.
    </p>
  </section>

  <section class="faq__item">
    <h3 class="faq__pregunta">¿Con cuánto tiempo de anticipación debo reservar?</h3>
    <p class="faq__respuesta">
      Te recomendamos hacer la reserva al menos con 7 a 10 días de anticipación, especialmente en temporada alta (ferias, festividades o lanzamientos). Esto nos permite garantizar disponibilidad y una correcta planificación logística.
    </p>
  </section>

  <section class="faq__item">
    <h3 class="faq__pregunta">¿Qué medios de pago aceptan?</h3>
    <p class="faq__respuesta">
      Aceptamos pagos vía transferencia bancaria, tarjetas de crédito/débito, y pagos con factura para empresas que cuenten con convenio. Consulta en <a href="/medios-de-pago">Medios de Pago</a> para más información.
    </p>
  </section>

  <section class="faq__item">
    <h3 class="faq__pregunta">¿Qué pasa si se daña un equipo durante el evento?</h3>
    <p class="faq__respuesta">
      Todos nuestros equipos cuentan con mantenimiento preventivo y son revisados antes de cada entrega. En caso de daños por mal uso o negligencia, se evalúa la situación y se aplica la cláusula de garantía según lo indicado en nuestros <a href="/terminos-y-condiciones">Términos y Condiciones</a>.
    </p>
  </section>

  <section class="faq__item">
