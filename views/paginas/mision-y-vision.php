  <h2 class="mision__heading">Misión, Visión y Valores</h2>

  <h3>Misión</h3>
  <p class="mision__parrafo">
    En <strong>VisualRent.cl</strong>, nuestra misión es brindar <strong>experiencias tecnológicas inolvidables</strong> a través del <strong>arriendo de soluciones innovadoras</strong> para eventos. Nos enfocamos en entregar un servicio confiable, humano y personalizado, asegurando que cada proyecto supere las expectativas y potencie el impacto de nuestros clientes.
  </p>

  <h3>Visión</h3>
  <p class="mision__parrafo">
    Aspiramos a ser reconocidos como la <strong>empresa líder en arriendo tecnológico para eventos en Chile</strong>, destacando por nuestra excelencia, cercanía y constante innovación. Nuestra visión también nos proyecta hacia nuevos desafíos: <strong>expandirnos a nivel nacional e internacional</strong>, siempre manteniendo el sello de confianza que nos caracteriza.
  </p>

  <h3>Valores</h3>
  <ul>
    <li><strong>Innovación:</strong> Buscamos estar siempre a la vanguardia tecnológica para ofrecer soluciones modernas, impactantes y efectivas.</li>
    <li><strong>Responsabilidad:</strong> Cumplimos nuestros compromisos con profesionalismo, puntualidad y transparencia en cada arriendo.</li>
    <li><strong>Compromiso:</strong> Nos involucramos con cada cliente como si su evento fuera nuestro, aportando ideas, soluciones y apoyo constante.</li>
    <li><strong>Calidad:</strong> Nuestros equipos, procesos y atención al cliente siguen altos estándares que garantizan una experiencia superior.</li>
    <li><strong>Cercanía:</strong> Creemos en relaciones genuinas y duraderas con nuestros clientes, construidas desde la confianza y el respeto.</li>
    <li><strong>Pasión:</strong> Amamos lo que hacemos, y eso se nota en cada detalle del servicio que entregamos.</li>
  </ul>
